    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        try {
            String eid = EidTf.getText();
            String ename = ExerciseTf.getText();
            
            ExerciseDTO exercise = new ExerciseDTO(eid, ename);
            boolean isAdded = exerciseController.add(exercise);
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "adding sucses");
                loadData();
            }else{
                JOptionPane.showMessageDialog(this, "adding faild");
            }
        } catch (Exception ex) {
            Logger.getLogger(ExersizePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
                
    }//GEN-LAST:event_BtnAddActionPerformed

    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        dtm.setRowCount(0);
        String id = EidTf.getText();
        String name = ExerciseTf.getText();
        
        ExerciseDTO exercise = new ExerciseDTO(id, name);
        
        try {
            boolean isUpdate = exerciseController.update(exercise);
            if (isUpdate) {
                dtm.setRowCount(0);
                
                ArrayList<ExerciseDTO> allExerciseDTOs = ctrlExercise.getAll();
                if (allExerciseDTOs == null) {
                    return;
                }
                for (ExerciseDTO exerciseDTO : allExerciseDTOs) {
                    Object[] rowData = {
                       exerciseDTO.getId(),
                       exerciseDTO.getName()
                    };
                    dtm.addRow(rowData);
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(ExersizePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_UpdateBtnActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        try {
            String EID = dtm.getValueAt(ExerciseTbl.getSelectedRow(),0).toString();
            boolean deleted = ctrlExercise.delete(EID);
            if (deleted) {
                JOptionPane.showMessageDialog(this, "Remove succsus");
                
                int index = ExerciseTbl.getSelectedRow();
                dtm.removeRow(index);
            }else{
                JOptionPane.showMessageDialog(this, "Remove Faild");
            }
        } catch (Exception ex) {
            Logger.getLogger(ExersizePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

