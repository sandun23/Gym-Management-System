    private void loadData(){
        dtm.setRowCount(0);
        
        try {
            ArrayList<ExerciseDTO> allExerciseDTOs = ctrlExercise.getAll();
            
            if (allExerciseDTOs == null) {
                return;
            }
            for (ExerciseDTO exercise : allExerciseDTOs) {
                Object[] rowData = {
                    exercise.getId(),
                    exercise.getName(),
                };
                dtm.addRow(rowData);
                
            }
        } catch (Exception ex) {
            Logger.getLogger(ExersizePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

