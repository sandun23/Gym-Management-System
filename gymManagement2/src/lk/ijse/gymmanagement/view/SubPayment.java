    private void loadMembershipNo(){
        try {
            ArrayList<RegistrationDTO> all = ctrlRegistration.getAll();
            comboMembership.removeAllItems();
            for(RegistrationDTO all2 : all){
                comboMembership.addItem(all2.getMembership_No());
            }
        } catch (Exception ex) {
            Logger.getLogger(AttendencePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    private void loadData(){
        try {
            dtmPayment.setRowCount(0);
            ArrayList<QueryPaymentDTO> queryPaymentDTOs = ctrlPayment.queryPaymentDTOs();
            
            for(QueryPaymentDTO queryPaymentDTO : queryPaymentDTOs){
                Object[] rowData = {
                    queryPaymentDTO.getMembership_No(),
                    queryPaymentDTO.getfName(),
                    queryPaymentDTO.getP_Date(),
                    queryPaymentDTO.getPID(),
                    queryPaymentDTO.getTypes(),
                    queryPaymentDTO.getAmount()
                    
                };
                dtmPayment.addRow(rowData);
            }
            
        } catch (Exception ex) {
            Logger.getLogger(SubPayment.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

