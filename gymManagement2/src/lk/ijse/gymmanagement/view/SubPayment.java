    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        
        String mid = comboMembership.getSelectedItem().toString();
        
        String date = dateTxt.getText();
        String Pid = pidTxt.getText();
        if (typeTxt.getSelectedItem().equals("Monthly")){
            type = "Monthly";
        }
        if (typeTxt.getSelectedItem().equals("Registration")){
            type = "Registration";
        }else{
            
        }
        double newAmount = Double.parseDouble(amountTxt.getText());

        
        PaymentDTO payment = new PaymentDTO(Pid, date, type, newAmount, mid);
        try {
            boolean isAdd = paymentController.add(payment);
            if (isAdd) {
                JOptionPane.showMessageDialog(this, "Succsus");
                loadData();
            }else{
                JOptionPane.showMessageDialog(this, "Faild");
            }
        } catch (Exception ex) {
            Logger.getLogger(SubPayment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

